CREATE TABLE public.schools (
  id uuid NOT NULL DEFAULT gen_random_uuid(),
  organization_id uuid,
  name text NOT NULL,
  slug text NOT NULL UNIQUE,
  created_at timestamp with time zone DEFAULT now(),
  invite_code text UNIQUE,
  CONSTRAINT schools_pkey PRIMARY KEY (id),
  CONSTRAINT schools_organization_id_fkey FOREIGN KEY (organization_id) REFERENCES public.organizations(id)
);
CREATE TABLE public.security_logs (
  id integer NOT NULL DEFAULT nextval('security_logs_id_seq'::regclass),
  user_id uuid,
  event_type character varying NOT NULL,
  event_description text,
  ip_address inet,
  user_agent text,
  metadata jsonb,
  created_at timestamp with time zone DEFAULT now(),
  CONSTRAINT security_logs_pkey PRIMARY KEY (id),
  CONSTRAINT security_logs_user_id_fkey FOREIGN KEY (user_id) REFERENCES auth.users(id)
);
CREATE TABLE public.teacher_classrooms (
  id uuid NOT NULL DEFAULT gen_random_uuid(),
  teacher_id uuid NOT NULL,
  classroom_id uuid NOT NULL,
  subject text NOT NULL,
  created_at timestamp with time zone DEFAULT now(),
  is_owner boolean DEFAULT false,
  CONSTRAINT teacher_classrooms_pkey PRIMARY KEY (id),
  CONSTRAINT teacher_classrooms_teacher_id_fkey FOREIGN KEY (teacher_id) REFERENCES public.profiles(id),
  CONSTRAINT teacher_classrooms_classroom_id_fkey FOREIGN KEY (classroom_id) REFERENCES public.classrooms(id)
);
